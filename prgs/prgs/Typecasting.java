   int b= 2;
   b= a+b;
   System.out.println("b= "+b);
   float c= 8;
   int d= 6;
   c= c+d;
   System.out.println("c= "+c);
   float e= 4;
   short f=3;
   e=e+f;
   System.out.println("e= "+e);
   short g= 2;
   long h= 3;
   h= g+h;
   System.out.println("h= "+h);
   long i = 54;
   char j= 'a';
   i= i+j;
   System.out.println("i= "+i);
