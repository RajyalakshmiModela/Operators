class logical
{
   public static void main(String[] args)
{
   int a=5,b=7;
   System.out.println("logical operator AND");
   System.out.println((a>b)&&(a<b));//false//any two or more conditions true it is true
   System.out.println((a<b)&&(a<b));//true
   System.out.println("logical operator OR");
   System.out.println((a>b)||(a>b));//false//any one condition is true it is a true
   System.out.println((a>b)||(a<b));//true
   System.out.println("logical operator NOT");
   System.out.println(!(a>b));//true//if false value is trueand true value is false

  
}
}
  
   